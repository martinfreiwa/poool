async fn dispatch_wise(
    input: PayoutDispatch,
    destination_email: &str,
) -> Result<DispatchResult, AppError> {
    // Phase-6: real Wise (TransferWise) Transfers API integration.
    //
    // Multi-step:
    //   1. POST /v1/accounts                 → create recipient by email
    //   2. POST /v3/profiles/{p}/quotes      → quote source → target amount
    //   3. POST /v1/transfers                → create transfer
    //   4. POST /v3/profiles/{p}/transfers/{t}/payments → fund via balance
    //
    // Env:
    //   WISE_API_TOKEN   — required (Bearer)
    //   WISE_PROFILE_ID  — required (the platform's business profile)
    //   WISE_API_BASE    — defaults to sandbox
    //                      (https://api.sandbox.transferwise.tech)
    //                      Set to https://api.transferwise.com for prod.
    let token = match std::env::var("WISE_API_TOKEN") {
        Ok(v) if !v.trim().is_empty() => v,
        _ => return wise_mocked(input, destination_email, "WISE_API_TOKEN not set"),
    };
    let profile_id: i64 = match std::env::var("WISE_PROFILE_ID")
        .ok()
        .and_then(|s| s.parse().ok())
    {
        Some(v) => v,
        None => return wise_mocked(input, destination_email, "WISE_PROFILE_ID not set"),
    };
    let api_base = std::env::var("WISE_API_BASE")
        .unwrap_or_else(|_| "https://api.sandbox.transferwise.tech".into());

    let client = reqwest::Client::builder()
        .timeout(std::time::Duration::from_secs(30))
        .build()
        .map_err(|e| AppError::Internal(format!("wise http client: {}", e)))?;

    // Convert cents → decimal string (Wise wants major units).
    let target_amount: f64 = (input.amount_cents as f64) / 100.0;

    // 1. Recipient. We use the email-based recipient type Wise calls
    //    "EMAIL" — Wise emails the recipient to claim funds. Simpler
    //    than full bank-detail collection; Wise handles KYC + payout.
    let recipient_body = serde_json::json!({
        "profile": profile_id,
        "accountHolderName": destination_email,
        "currency": input.currency,
        "type": "EMAIL",
        "details": { "email": destination_email }
    });
    let recipient_id: i64 = match client
        .post(format!("{}/v1/accounts", api_base))
        .bearer_auth(&token)
        .json(&recipient_body)
        .send()
        .await
    {
        Ok(r) if r.status().is_success() => {
            let v: serde_json::Value = r.json().await.unwrap_or_default();
            v.get("id").and_then(|i| i.as_i64()).unwrap_or(0)
        }
        Ok(r) => {
            let status = r.status();
            let txt = r.text().await.unwrap_or_default();
            tracing::error!("Wise recipient-create failed: {} — {}", status, txt);
            return Ok(wise_fail(format!("Wise recipient-create: HTTP {}", status)));
        }
        Err(e) => return Ok(wise_fail(format!("Wise recipient network: {}", e))),
    };
    if recipient_id == 0 {
        return Ok(wise_fail("Wise recipient-create returned no id".into()));
    }

    // 2. Quote. We pin target-amount (the affiliate receives a known
    //    sum); Wise computes the source debit + fee.
    let quote_body = serde_json::json!({
        "sourceCurrency": "EUR",
        "targetCurrency": input.currency,
        "targetAmount": target_amount,
        "payOut": "BALANCE",
        "preferredPayIn": "BALANCE",
    });
    let quote_id: String = match client
        .post(format!("{}/v3/profiles/{}/quotes", api_base, profile_id))
        .bearer_auth(&token)
        .json(&quote_body)
        .send()
        .await
    {
        Ok(r) if r.status().is_success() => {
            let v: serde_json::Value = r.json().await.unwrap_or_default();
            v.get("id")
                .and_then(|i| i.as_str())
                .unwrap_or("")
                .to_string()
        }
        Ok(r) => {
            let status = r.status();
            let txt = r.text().await.unwrap_or_default();
            tracing::error!("Wise quote failed: {} — {}", status, txt);
            return Ok(wise_fail(format!("Wise quote: HTTP {}", status)));
        }
        Err(e) => return Ok(wise_fail(format!("Wise quote network: {}", e))),
    };
    if quote_id.is_empty() {
        return Ok(wise_fail("Wise quote returned no id".into()));
    }

    // 3. Transfer. customerTransactionId = idempotency key.
    let transfer_body = serde_json::json!({
        "targetAccount": recipient_id,
        "quoteUuid": quote_id,
        "customerTransactionId": format!("poool-aff-{}", input.reference),
        "details": {
            "reference": input.reference,
            "sourceOfFunds": "verification.source.of.funds.other",
        }
    });
    let transfer_id: i64 = match client
        .post(format!("{}/v1/transfers", api_base))
        .bearer_auth(&token)
        .json(&transfer_body)
        .send()
        .await
    {
        Ok(r) if r.status().is_success() => {
            let v: serde_json::Value = r.json().await.unwrap_or_default();
            v.get("id").and_then(|i| i.as_i64()).unwrap_or(0)
        }
        Ok(r) => {
            let status = r.status();
            let txt = r.text().await.unwrap_or_default();
            tracing::error!("Wise transfer-create failed: {} — {}", status, txt);
            return Ok(wise_fail(format!("Wise transfer-create: HTTP {}", status)));
        }
        Err(e) => return Ok(wise_fail(format!("Wise transfer network: {}", e))),
    };
    if transfer_id == 0 {
        return Ok(wise_fail("Wise transfer-create returned no id".into()));
    }

    // 4. Fund.
    let fund_res = client
        .post(format!(
            "{}/v3/profiles/{}/transfers/{}/payments",
            api_base, profile_id, transfer_id
        ))
        .bearer_auth(&token)
        .json(&serde_json::json!({ "type": "BALANCE" }))
        .send()
        .await;
    match fund_res {
        Ok(r) if r.status().is_success() => {
            tracing::info!(
                affiliate_id = %input.affiliate_id,
                wise_transfer_id = transfer_id,
                amount_cents = input.amount_cents,
                "[FINANCIAL] Wise transfer funded"
            );
            Ok(DispatchResult {
                method_type: "wise_email".into(),
                provider_reference: transfer_id.to_string(),
                status: DispatchStatus::Accepted,
                message: "Wise transfer funded; recipient claims via email.".into(),
            })
        }
        Ok(r) => {
            let status = r.status();
            let txt = r.text().await.unwrap_or_default();
            tracing::error!("Wise fund failed: {} — {}", status, txt);
            Ok(wise_fail(format!("Wise fund: HTTP {}", status)))
        }
        Err(e) => Ok(wise_fail(format!("Wise fund network: {}", e))),
    }
}

