//! Phase-5: payout connector dispatch.
//!
//! Reads the affiliate's default `affiliate_payout_methods` row and
//! dispatches the actual transfer to the right downstream connector.
//! The SEPA path remains internal (covered by `service::generate_sepa_pain001_for_batch`
//! producing an XML the operator uploads to their bank); PayPal / Wise /
//! Stripe Connect / USDC are external API calls.
//!
//! THIS MODULE IS A SCAFFOLD. Each non-SEPA branch logs the intended
//! call + returns a `Mocked` reference so the admin payout-batch flow
//! is exercise-able end-to-end. Wiring the real API client (auth + body
//! shape per provider) is intentionally deferred to a follow-up task —
//! the dispatch surface here is stable so the admin batch handler can
//! adopt it now.
//!
//! How to use:
//! ```ignore
//! let res = payout_connectors::dispatch_payout(&pool, PayoutDispatch {
//!     affiliate_id, amount_cents: 12_345, currency: "EUR".into(),
//!     reference: "batch:abc".into(),
//! }).await?;
//! // res.method_type tells you which connector handled it,
//! // res.provider_reference is the bank/PayPal/Wise/etc tx id.
//! ```

use crate::error::AppError;
use serde::Serialize;
use sqlx::PgPool;
use uuid::Uuid;

#[derive(Debug, Clone)]
pub struct PayoutDispatch {
    pub affiliate_id: Uuid,
    pub amount_cents: i64,
    pub currency: String,
    /// Free-form operator reference shown on the bank statement / PayPal
    /// note. Usually `"poool-batch-{batch_id_short}"`.
    pub reference: String,
}

#[derive(Debug, Serialize)]
pub struct DispatchResult {
    pub method_type: String,
    /// Provider-side reference (Wise transfer id, PayPal payout-batch-id, …).
    /// Empty string for SEPA (operator-driven) until they file the XML.
    pub provider_reference: String,
    pub status: DispatchStatus,
    pub message: String,
}

#[derive(Debug, Serialize, PartialEq, Eq)]
#[serde(rename_all = "snake_case")]
pub enum DispatchStatus {
    /// Connector accepted the transfer; provider_reference set.
    Accepted,
    /// SEPA-only: operator will file the XML batch manually.
    PendingOperator,
    /// Connector failed (network, auth, validation, etc.).
    Failed,
    /// Connector is a scaffold — call site recognises this and falls back
    /// to internal SEPA / operator escalation.
    Mocked,
}

/// Look up the default active payout method for `affiliate_id` and call
/// the matching connector. Returns `Failed` with a descriptive message
/// if no default method is configured (caller decides whether to fall
/// back to the legacy `developer_teams.bank_iban_*` path).
pub async fn dispatch_payout(
    pool: &PgPool,
    input: PayoutDispatch,
) -> Result<DispatchResult, AppError> {
    let method = sqlx::query!(
        r#"SELECT id, method_type, identifier_encrypted, identifier_plain
             FROM affiliate_payout_methods
            WHERE affiliate_id = $1 AND is_default = TRUE AND is_active = TRUE
            LIMIT 1"#,
        input.affiliate_id
    )
    .fetch_optional(pool)
    .await?;

    let Some(m) = method else {
        return Ok(DispatchResult {
            method_type: "none".into(),
            provider_reference: String::new(),
            status: DispatchStatus::Failed,
            message: "No default payout method configured. Falling back to legacy SEPA path."
                .into(),
        });
    };

    match m.method_type.as_str() {
        "sepa_iban" => dispatch_sepa(input).await,
        "paypal_email" => {
            let dest = m.identifier_plain.unwrap_or_default();
            dispatch_paypal(input, &dest).await
        }
        "wise_email" => {
            let dest = m.identifier_plain.unwrap_or_default();
            dispatch_wise(input, &dest).await
        }
        "usdc_wallet" => {
            let dest = m.identifier_plain.unwrap_or_default();
            dispatch_usdc(input, &dest).await
        }
        "stripe_connect" => {
            let dest = m.identifier_plain.unwrap_or_default();
            dispatch_stripe(input, &dest).await
        }
        other => Ok(DispatchResult {
            method_type: other.into(),
            provider_reference: String::new(),
            status: DispatchStatus::Failed,
            message: format!("Unknown method_type {}", other),
        }),
    }
}

async fn dispatch_sepa(input: PayoutDispatch) -> Result<DispatchResult, AppError> {
    // SEPA is operator-driven: the batch XML is generated by
    // `service::generate_sepa_pain001_for_batch` and uploaded to the
    // bank portal. The dispatch result here just acknowledges that
    // the operator will pick it up. The actual money movement
    // happens hours later.
    tracing::info!(
        affiliate_id = %input.affiliate_id,
        amount_cents = input.amount_cents,
        currency = %input.currency,
        ref = %input.reference,
        "SEPA payout queued for operator (pain.001 export)"
    );
    Ok(DispatchResult {
        method_type: "sepa_iban".into(),
        provider_reference: String::new(),
        status: DispatchStatus::PendingOperator,
        message: "Queued for SEPA pain.001 batch export.".into(),
    })
}

async fn dispatch_paypal(
    input: PayoutDispatch,
    destination_email: &str,
) -> Result<DispatchResult, AppError> {
    // Phase-6: real PayPal Payouts API integration.
    //
    // Two-step:
    //   1. OAuth: POST /v1/oauth2/token (grant_type=client_credentials,
    //      Basic auth client_id:secret) → access_token
    //   2. Payout: POST /v1/payments/payouts with the access_token
    //
    // Env:
    //   PAYPAL_CLIENT_ID, PAYPAL_CLIENT_SECRET — required
    //   PAYPAL_API_BASE                       — defaults to sandbox
    //                                             (https://api-m.sandbox.paypal.com)
    //                                             Set to https://api-m.paypal.com
    //                                             for prod.
    //
    // When the env vars are missing we fall back to `Mocked` so dev /
    // CI environments don't blow up on real money calls.
    let client_id = match std::env::var("PAYPAL_CLIENT_ID") {
        Ok(v) if !v.trim().is_empty() => v,
        _ => return paypal_mocked(input, destination_email, "PAYPAL_CLIENT_ID not set"),
    };
    let client_secret = match std::env::var("PAYPAL_CLIENT_SECRET") {
        Ok(v) if !v.trim().is_empty() => v,
        _ => return paypal_mocked(input, destination_email, "PAYPAL_CLIENT_SECRET not set"),
    };
    let api_base = std::env::var("PAYPAL_API_BASE")
        .unwrap_or_else(|_| "https://api-m.sandbox.paypal.com".into());

    let client = reqwest::Client::builder()
        .timeout(std::time::Duration::from_secs(20))
        .build()
        .map_err(|e| AppError::Internal(format!("paypal http client: {}", e)))?;

    // 1. OAuth
    let token_res = client
        .post(format!("{}/v1/oauth2/token", api_base))
        .basic_auth(&client_id, Some(&client_secret))
        .form(&[("grant_type", "client_credentials")])
        .send()
        .await;
    let access_token = match token_res {
        Ok(r) if r.status().is_success() => {
            let v: serde_json::Value = r.json().await.unwrap_or_default();
            v.get("access_token")
                .and_then(|t| t.as_str())
                .map(|s| s.to_string())
                .unwrap_or_default()
        }
        Ok(r) => {
            let status = r.status();
            let body = r.text().await.unwrap_or_default();
            tracing::error!("PayPal OAuth failed: {} — {}", status, body);
            return Ok(DispatchResult {
                method_type: "paypal_email".into(),
                provider_reference: String::new(),
                status: DispatchStatus::Failed,
                message: format!("PayPal OAuth failed: HTTP {}", status),
            });
        }
        Err(e) => {
            return Ok(DispatchResult {
                method_type: "paypal_email".into(),
                provider_reference: String::new(),
                status: DispatchStatus::Failed,
                message: format!("PayPal OAuth network error: {}", e),
            });
        }
    };
    if access_token.is_empty() {
        return Ok(DispatchResult {
            method_type: "paypal_email".into(),
            provider_reference: String::new(),
            status: DispatchStatus::Failed,
            message: "PayPal OAuth response missing access_token".into(),
        });
    }

    // 2. Payout. PayPal expects whole + cents in the "value" string,
    //    formatted to 2dp. The currency code goes alongside.
    let amount_str = format!(
        "{}.{:02}",
        input.amount_cents / 100,
        input.amount_cents.abs() % 100
    );
    let sender_batch_id = format!("poool-aff-{}", input.reference);
    let body = serde_json::json!({
        "sender_batch_header": {
            "sender_batch_id": sender_batch_id,
            "email_subject": "Your POOOL affiliate payout",
            "email_message": format!("Affiliate payout from POOOL: {} {}",
                amount_str, input.currency),
        },
        "items": [{
            "recipient_type": "EMAIL",
            "receiver": destination_email,
            "amount": { "value": amount_str, "currency": input.currency },
            "sender_item_id": input.reference,
            "note": format!("POOOL affiliate commission payout — ref {}", input.reference),
        }]
    });

    let payout_res = client
        .post(format!("{}/v1/payments/payouts", api_base))
        .bearer_auth(&access_token)
        .json(&body)
        .send()
        .await;
    match payout_res {
        Ok(r) if r.status().is_success() || r.status().as_u16() == 201 => {
            let v: serde_json::Value = r.json().await.unwrap_or_default();
            let batch_id = v
                .pointer("/batch_header/payout_batch_id")
                .and_then(|s| s.as_str())
                .unwrap_or("")
                .to_string();
            tracing::info!(
                affiliate_id = %input.affiliate_id,
                payout_batch_id = %batch_id,
                amount_cents = input.amount_cents,
                "[FINANCIAL] PayPal payout submitted"
            );
            Ok(DispatchResult {
                method_type: "paypal_email".into(),
                provider_reference: batch_id,
                status: DispatchStatus::Accepted,
                message: "PayPal payout accepted; final settlement is async.".into(),
            })
        }
        Ok(r) => {
            let status = r.status();
            let body_txt = r.text().await.unwrap_or_default();
            tracing::error!("PayPal payout failed: {} — {}", status, body_txt);
            Ok(DispatchResult {
                method_type: "paypal_email".into(),
                provider_reference: String::new(),
                status: DispatchStatus::Failed,
                message: format!("PayPal payout failed: HTTP {}", status),
            })
        }
        Err(e) => Ok(DispatchResult {
            method_type: "paypal_email".into(),
            provider_reference: String::new(),
            status: DispatchStatus::Failed,
            message: format!("PayPal network error: {}", e),
        }),
    }
}

