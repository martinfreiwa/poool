async fn dispatch_stripe(
    input: PayoutDispatch,
    destination_account: &str,
) -> Result<DispatchResult, AppError> {
    // Phase-6: real Stripe Connect Transfer API integration.
    //
    // POST https://api.stripe.com/v1/transfers
    //   Authorization: Bearer <STRIPE_SECRET_KEY>
    //   Body (form-encoded): amount=<cents>, currency=<lower3>,
    //     destination=<acct_xxx>, transfer_group=<reference>,
    //     description=<note>
    //
    // Stripe accepts integer minor units directly (cents) so no
    // formatting needed. Currency must be lowercase per their spec.
    let secret_key = match std::env::var("STRIPE_SECRET_KEY") {
        Ok(v) if !v.trim().is_empty() => v,
        _ => return stripe_mocked(input, destination_account, "STRIPE_SECRET_KEY not set"),
    };

    if !destination_account.starts_with("acct_") {
        return Ok(DispatchResult {
            method_type: "stripe_connect".into(),
            provider_reference: String::new(),
            status: DispatchStatus::Failed,
            message: "Stripe destination must start with acct_".into(),
        });
    }

    let client = reqwest::Client::builder()
        .timeout(std::time::Duration::from_secs(20))
        .build()
        .map_err(|e| AppError::Internal(format!("stripe http client: {}", e)))?;

    // Idempotency key — Stripe deduplicates identical requests with the
    // same key for 24h. The batch reference is unique per (batch, affiliate).
    let idempotency_key = format!("poool-aff-{}", input.reference);
    let currency_lower = input.currency.to_ascii_lowercase();
    let form = [
        ("amount", input.amount_cents.to_string()),
        ("currency", currency_lower),
        ("destination", destination_account.to_string()),
        ("transfer_group", input.reference.clone()),
        (
            "description",
            format!("POOOL affiliate commission — ref {}", input.reference),
        ),
    ];

    let res = client
        .post("https://api.stripe.com/v1/transfers")
        .bearer_auth(&secret_key)
        .header("Idempotency-Key", &idempotency_key)
        .form(&form)
        .send()
        .await;
    match res {
        Ok(r) if r.status().is_success() => {
            let v: serde_json::Value = r.json().await.unwrap_or_default();
            let transfer_id = v
                .get("id")
                .and_then(|s| s.as_str())
                .unwrap_or("")
                .to_string();
            tracing::info!(
                affiliate_id = %input.affiliate_id,
                stripe_transfer_id = %transfer_id,
                amount_cents = input.amount_cents,
                "[FINANCIAL] Stripe Connect transfer submitted"
            );
            Ok(DispatchResult {
                method_type: "stripe_connect".into(),
                provider_reference: transfer_id,
                status: DispatchStatus::Accepted,
                message: "Stripe transfer created.".into(),
            })
        }
        Ok(r) => {
            let status = r.status();
            let body_txt = r.text().await.unwrap_or_default();
            tracing::error!("Stripe transfer failed: {} — {}", status, body_txt);
            Ok(DispatchResult {
                method_type: "stripe_connect".into(),
                provider_reference: String::new(),
                status: DispatchStatus::Failed,
                message: format!("Stripe transfer failed: HTTP {}", status),
            })
        }
        Err(e) => Ok(DispatchResult {
            method_type: "stripe_connect".into(),
            provider_reference: String::new(),
            status: DispatchStatus::Failed,
            message: format!("Stripe network error: {}", e),
        }),
    }
}

